#program in which user enters a number and then print its interval
num=int(input("Enter the number from 0-30:"))
if(num>=0 and num<10):
    print("It is in the range of 0 to 10")
if(num>=10 and num<20):
    print("It is in the range of 10 and 20")
if(num>=20 and num<30):
    print("It is in the range of 20 and 30")